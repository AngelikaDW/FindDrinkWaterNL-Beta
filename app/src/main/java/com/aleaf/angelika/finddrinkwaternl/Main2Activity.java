public class Main2Activity extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main2);
        Toolbar toolbar = (Toolbar) findViewById(R.id.toolbar);
        setSupportActionBar(toolbar);
    }

    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        MenuInflater inflater = getMenuInflater();
        inflater.inflate(R.menu.main_menu, menu);
        return true;
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        //Handle item selection
        switch (item.getItemId()) {
            case R.id.info_water:
                Intent infoIntent = new Intent(Main2Activity.this, InfoActivity.class);
                startActivity(infoIntent);
                Toast.makeText(this, "Info was clicked", Toast.LENGTH_SHORT).show();
                return true;
            case R.id.colofon:
                Intent colofonIntent = new Intent(Main2Activity.this, InfoActivity.class);
                startActivity(colofonIntent);
                Toast.makeText(this, "Colofon was clicked", Toast.LENGTH_SHORT).show();
                return true;
            default:
                return super.onOptionsItemSelected(item);
        }
    }

}